public class Apple {
	
	private Integer weight;
	private String color;
	
	public Apple(){}
	
	public Apple(Integer weight) {
		this.weight = weight;
	}
	
	public Integer getWeight() {
		return weight;
	}
	
	public String getColor() {
		return color;
	}
}
